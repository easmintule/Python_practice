print("course3 :" + course1[0]) # 0 number index will show that means 'python' index [012345]= 'P'

#course4 = "Python for beginners"
print("course4 :" + course1[-1]) # last word 'beginners' = 's' will show

#course5 = "Python for beginners"
print("course5 :" + course1[0 : 3 ]) #course5 :Pyt; 0 will assume as the start index and lenght of the string will assume as the end index

